	private Global.PresenceStatus GetStatus_Internal(BnetPlayer player, ref string statusKey, List<string> stringArgs = null, List<Enum> enumVals = null)
	{
		Global.PresenceStatus status = Global.PresenceStatus.UNKNOWN;
		if (player == null)
		{
			return status;
		}
		if (player.GetBestGameAccount() == null)
		{
			return status;
		}
		BnetGameAccount hsGameAccount = player.GetHearthstoneGameAccount();
		if (hsGameAccount == null)
		{
			return status;
		}
		if (!hsGameAccount.TryGetGameFieldBytes(17u, out var bytes))
		{
			return status;
		}
		return GetStatus_Internal(bytes, ref statusKey, stringArgs, enumVals);
	}

	private Global.PresenceStatus GetStatus_Internal(byte[] bytes, ref string statusKey, List<string> stringArgs = null, List<Enum> enumVals = null)
	{
		Global.PresenceStatus status = Global.PresenceStatus.UNKNOWN;
		if (bytes == null)
		{
			return status;
		}
		Enum currentEnumVal = null;
		using (MemoryStream memStream = new MemoryStream(bytes))
		{
			using BinaryReader reader = new BinaryReader(memStream);
			if (!DecodeStatusVal(reader, ref currentEnumVal, ref statusKey))
			{
				return status;
			}
			status = (Global.PresenceStatus)(int)(object)currentEnumVal;
			enumVals?.Add(status);
			if (stringArgs != null || enumVals != null)
			{
				while (memStream.Position < bytes.Length)
				{
					string currentKey = null;
					if (!DecodeStatusVal(reader, ref currentEnumVal, ref currentKey))
					{
						return status;
					}
					enumVals?.Add(currentEnumVal);
					if (stringArgs != null)
					{
						string text = GameStrings.Get(currentKey);
						stringArgs.Add(text);
					}
				}
			}
		}
		return status;
	}

	private void Initialize()
	{
		for (int i = 0; i < s_enumIdList.Length; i++)
		{
			Type type = s_enumIdList[i];
			if (Enum.GetUnderlyingType(type) != typeof(int))
			{
				throw new Exception($"Underlying type of enum {type.FullName} (underlying={Enum.GetUnderlyingType(type).FullName}) must {typeof(int).Name} be to used by Presence system.");
			}
			byte id = (byte)(i + 1);
			m_enumToIdMap.Add(type, id);
			m_idToEnumMap.Add(id, type);
		}
	}

	private bool SetStatusImpl(Enum[] status)
	{
		if (!Network.ShouldBeConnectedToAurora())
		{
			return false;
		}
		if (!Network.IsLoggedIn())
		{
			return true;
		}
		if (status == null || status.Length == 0)
		{
			Error.AddDevFatal("PresenceMgr.SetStatusImpl() - Received status of length 0. Setting empty status is not supported.");
			return false;
		}
		if (GeneralUtils.AreArraysEqual(m_status, status))
		{
			return true;
		}
		if (!SetRichPresence(status))
		{
			return false;
		}
		if (!SetGamePresence(status))
		{
			return false;
		}
		NetCache.NetCacheFeatures features = NetCache.Get().GetNetObject<NetCache.NetCacheFeatures>();
		if (features != null && features.SendTelemetryPresence)
		{
			ReportPresenceToTelemetry(status);
		}
		m_prevStatus = m_status;
		bool num = m_prevStatus == null || m_prevStatus.Length == 0;
		m_status = new Enum[status.Length];
		Array.Copy(status, m_status, status.Length);
		if (num || !IsStatusPlayingGame((Global.PresenceStatus)(object)status[0]))
		{
			SpectatorManager.Get().UpdateMySpectatorInfo();
		}
		return true;
	}

	private void ReportPresenceToTelemetry(Enum[] status)
	{
		if (status.Length != 0)
		{
			long currentTime = (long)(Time.realtimeSinceStartup * 1000f);
			long durationMs = currentTime - m_timeStartStatusMs;
			m_timeStartStatusMs = currentTime;
			PresenceStatus newStatus = new PresenceStatus
			{
				PresenceId = (long)(Global.PresenceStatus)(object)status[0]
			};
			if (status.Length > 1)
			{
				newStatus.PresenceSubId = Convert.ToInt64(status.Skip(1).FirstOrDefault());
			}
			TelemetryManager.Client().SendPresenceChanged(newStatus, m_currentStatus, durationMs);
			m_timeStartStatusMs = currentTime;
			m_currentStatus = newStatus;
		}
	}

	private bool SetRichPresence(Enum[] status)
	{
		Enum[] richPresence = new Enum[status.Length];
		for (int i = 0; i < status.Length; i++)
		{
			Enum statusVal = status[i];
			if (s_richPresenceMap.TryGetValue(statusVal, out var richPresenceVal))
			{
				if (richPresenceVal == null)
				{
					return false;
				}
			}
			else
			{
				richPresenceVal = statusVal;
			}
			richPresence[i] = richPresenceVal;
		}
		if (richPresence.Any((Enum e) => !RichPresence.s_streamIds.ContainsKey(e.GetType())))
		{
			richPresence = new Enum[1] { richPresence[0] };
		}
		if (GeneralUtils.AreArraysEqual(m_richPresence, richPresence))
		{
			return true;
		}
		m_richPresence = richPresence;
		if (!Network.ShouldBeConnectedToAurora())
		{
			Error.AddDevFatal(string.Format("Caller should check for Battle.net connection before calling SetRichPresence {0}", (richPresence == null) ? "" : string.Join(", ", richPresence.Select((Enum x) => x.ToString()).ToArray())));
			return false;
		}
		if (richPresence == null)
		{
			return false;
		}
		if (richPresence.Length == 0)
		{
			return false;
		}
		RichPresenceUpdate[] updates = new RichPresenceUpdate[richPresence.Length];
		for (int j = 0; j < richPresence.Length; j++)
		{
			Enum richPresenceVal2 = richPresence[j];
			Type type = richPresenceVal2.GetType();
			FourCC streamId = RichPresence.s_streamIds[type];
			RichPresenceUpdate update = default(RichPresenceUpdate);
			update.presenceFieldIndex = ((j != 0) ? ((uint)(458752 + j)) : 0u);
			update.programId = BnetProgramId.HEARTHSTONE.GetValue();
			update.streamId = streamId.GetValue();
			update.index = Convert.ToUInt32(richPresenceVal2);
			updates[j] = update;
		}
		BattleNet.SetRichPresence(updates);
		return true;
	}

	private bool SetGamePresence(Enum[] status)
	{
		using MemoryStream memStream = new MemoryStream();
		using BinaryWriter writer = new BinaryWriter(memStream);
		for (int i = 0; i < status.Length; i++)
		{
			if (!EncodeStatusVal(status, i, out var id, out var intVal))
			{
				return false;
			}
			writer.Write(id);
			writer.Write(intVal);
		}
		byte[] buffer = memStream.GetBuffer();
		byte[] bytes = new byte[memStream.Position];
		Array.Copy(buffer, bytes, bytes.Length);
		return BnetPresenceMgr.Get().SetGameField(17u, bytes);
	}

	private bool EncodeStatusVal(Enum[] status, int index, out byte id, out int intVal)
	{
		Enum val = status[index];
		Type type = val.GetType();
		intVal = Convert.ToInt32(val);
		if (!m_enumToIdMap.TryGetValue(type, out id))
		{
			Error.AddDevFatal("PresenceMgr.EncodeStatusVal() - {0} at index {1} belongs to type {2}, which has no id", val, index, type);
			return false;
		}
		return true;
	}

	private bool DecodeStatusVal(BinaryReader reader, ref Enum enumVal, ref string key)
	{
		key = null;
		byte id = 0;
		int intVal = 0;
		int indexId = (int)reader.BaseStream.Position;
		int indexVal = indexId + 1;
		try
		{
			id = reader.ReadByte();
			indexVal = (int)reader.BaseStream.Position;
		}
		catch (Exception ex)
		{
			Log.Presence.Print("PresenceMgr.DecodeStatusVal - unable to decode enum id {0} at index {1} : {2} {3}", id, indexId, ex.GetType().FullName, ex.Message);
			return false;
		}
		if (!m_idToEnumMap.TryGetValue(id, out var type))
		{
			Log.Presence.Print("PresenceMgr.DecodeStatusVal - id {0} at index {1}, has no enum type", id, indexId);
			return false;
		}
		try
		{
			intVal = reader.ReadInt32();
		}
		catch (Exception ex2)
		{
			Log.Presence.Print("PresenceMgr.DecodeStatusVal - unable to decode enum value {0} at index {1} : {2} {3}", id, indexVal, ex2.GetType().FullName, ex2.Message);
			return false;
		}
		if (type == typeof(Global.PresenceStatus))
		{
			Global.PresenceStatus status = (Global.PresenceStatus)intVal;
			enumVal = status;
			if (!s_stringKeyMap.TryGetValue(status, out key))
			{
				Log.Presence.Print("PresenceMgr.DecodeStatusVal - value {0}.{1} at index {2}, has no string", type, status, indexVal);
				return false;
			}
		}
		else if (type == typeof(PresenceTutorial))
		{
			PresenceTutorial tutorial = (PresenceTutorial)intVal;
			enumVal = tutorial;
			if (!s_stringKeyMap.TryGetValue(tutorial, out key))
			{
				Log.Presence.Print("PresenceMgr.DecodeStatusVal - value {0}.{1} at index {2}, has no string", type, tutorial, indexVal);
				return false;
			}
		}
		else if (type == typeof(PresenceAdventureMode))
		{
			PresenceAdventureMode modeId = (PresenceAdventureMode)intVal;
			enumVal = modeId;
			if (!s_stringKeyMap.TryGetValue(modeId, out key))
			{
				Log.Presence.Print("PresenceMgr.DecodeStatusVal - value {0}.{1} at index {2}, has no string", type, modeId, indexVal);
				return false;
			}
		}
		else if (type == typeof(ScenarioDbId))
		{
			ScenarioDbId missionId = (ScenarioDbId)intVal;
			enumVal = missionId;
			if (!s_stringKeyMap.TryGetValue(missionId, out key))
			{
				Log.Presence.Print("PresenceMgr.DecodeStatusVal - value {0}.{1} at index {2}, has no string", type, missionId, indexVal);
				return false;
			}
		}
		return true;
	}

